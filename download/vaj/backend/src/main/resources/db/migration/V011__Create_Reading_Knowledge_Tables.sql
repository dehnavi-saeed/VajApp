-- V011__Create_Reading_Sessions.sql
CREATE TABLE ReadingSessions (
    Id UNIQUEIDENTIFIER NOT NULL DEFAULT NEWID(),
    BookId UNIQUEIDENTIFIER NOT NULL,
    StartPage INT NULL,
    EndPage INT NULL,
    DurationMs BIGINT NULL,
    StartedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    EndedAt DATETIME2 NULL,
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    CreatedBy UNIQUEIDENTIFIER NULL,
    CONSTRAINT PK_ReadingSessions PRIMARY KEY (Id),
    CONSTRAINT FK_ReadingSessions_Books FOREIGN KEY (BookId) REFERENCES Books(Id),
    CONSTRAINT CK_ReadingSessions_Duration CHECK (DurationMs >= 0),
    CONSTRAINT CK_ReadingSessions_Pages CHECK (StartPage > 0 AND EndPage >= StartPage)
);
CREATE INDEX IX_ReadingSessions_BookId ON ReadingSessions (BookId);
CREATE INDEX IX_ReadingSessions_StartedAt ON ReadingSessions (StartedAt);

-- V012__Create_Bookmarks.sql
