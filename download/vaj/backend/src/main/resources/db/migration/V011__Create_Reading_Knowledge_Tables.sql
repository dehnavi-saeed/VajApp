    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    CreatedBy UNIQUEIDENTIFIER NULL,
    CONSTRAINT PK_Highlights PRIMARY KEY (Id),
    CONSTRAINT FK_Highlights_Books FOREIGN KEY (BookId) REFERENCES Books(Id),
