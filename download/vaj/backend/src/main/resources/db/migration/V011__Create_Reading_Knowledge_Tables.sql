-- V019__Create_Reading_Goals.sql
CREATE TABLE ReadingGoals (
    Id UNIQUEIDENTIFIER NOT NULL DEFAULT NEWID(),
    UserId UNIQUEIDENTIFIER NOT NULL,
    LibraryId UNIQUEIDENTIFIER NULL,
    Title NVARCHAR(200) NOT NULL,
    GoalType NVARCHAR(20) NOT NULL,
    TargetValue INT NOT NULL,
    CurrentValue INT NOT NULL DEFAULT 0,
    StartDate DATE NOT NULL,
    EndDate DATE NOT NULL,
    Status NVARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    CreatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    UpdatedAt DATETIME2 NOT NULL DEFAULT GETUTCDATE(),
    Version INT NOT NULL DEFAULT 0,
    CONSTRAINT PK_ReadingGoals PRIMARY KEY (Id),
    CONSTRAINT FK_ReadingGoals_Users FOREIGN KEY (UserId) REFERENCES Users(Id),
    CONSTRAINT FK_ReadingGoals_Libraries FOREIGN KEY (LibraryId) REFERENCES Libraries(Id),
    CONSTRAINT CK_ReadingGoals_Target CHECK (TargetValue > 0),
    CONSTRAINT CK_ReadingGoals_Current CHECK (CurrentValue >= 0)
);
CREATE INDEX IX_ReadingGoals_UserId ON ReadingGoals (UserId);
CREATE INDEX IX_ReadingGoals_Status ON ReadingGoals (Status);